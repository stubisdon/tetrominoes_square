char	*ft_strcpy(char *dest, char *src)
{
	int		i;
	char	*copy;

	i = 0;
	copy = dest;
	while (src[i] != '\0')
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	dest = copy;
	return (copy);
}
